 */
function decodeAuthString($authstring, $flagBase64 = false){

    $auth_str = ($flagBase64 ? base64_decode($authstring) : $this->decrypt($authstring));
        
    preg_match("/^([^\:]+)\:([\S ]+)$/i", $auth_str, $arrMatches);

    return Array($arrMatches[1], $arrMatches[2]);
}

