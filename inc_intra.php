private function getDefaultClass($title, $uri){
    $prfx = 'fa-';
    $allstr = $title.$uri;
    $filename = pathinfo(parse_url($uri, PHP_URL_PATH),PATHINFO_FILENAME);
    if(preg_match('/search/i', $allstr))
