                )
            : ""
            );
    $strRet .= ($strRet!="" && !empty($trnEndDate) && !($trnStartDate==$trnEndDate)
            ? " - "
            : "");
    $strRet .= (!empty($trnEndDate) && !($trnStartDate==$trnEndDate)
            ? ($precision == 'datetime' 
                ? $this->datetimeSQL2PHP($trnEndDate)
                : $this->dateSQL2PHP($trnEndDate)
                )
            : ""
            );
    
    return $strRet;
}

