public static function isRecursion(){
    $aCallsUnique = array();
    foreach (debug_backtrace() as $call) {
        $class_func_curr = (isset($call['class']) ? $call['class'].'::' : '').$call['function'];
        $aCallsUnique[$class_func_curr] = (isset($aCallsUnique[$class_func_curr]) ? $aCallsUnique[$class_func_curr] : 0) + 1;
    }
    foreach($aCallsUnique as $nCalls){
        if($nCalls>1)
            return true;
    }
    return false;
}

/**
 * This function returns date and time string basing on operation date and time string. 
 * If operation day start time is greater than operation day end time, and time passed less than operation day end time,
 * then date will be incremented by one day.
 * 
 * For example, if operation day starts at 07:30 and ends at 01:30 next day, then operation time '01:00' will be considered as next day time. If operation date is '2024-06-30', then returned date and time will be '2024-07-01 01:00:00'.
 *
 * @category Data Output
 *
 * @param string $operationDate - date in 'Y-m-d' format
 * @param string $time - time in 'H:i:s' format
 * 
 * @return string date and time in 'Y-m-d H:i:s' format
 */
function getDateTimeByOperationTime($operationDate, $time){

    $stpOperationDayStart = isset($this->conf['stpOperationDayStart']) ? $this->conf['stpOperationDayStart'] : '00:00'; 
    $stpOperationDayEnd = isset($this->conf['stpOperationDayEnd']) ? $this->conf['stpOperationDayEnd'] : '23:59:59'; 
    $tempDate = Date('Y-m-d');
    if (strtotime($tempDate.' '.$stpOperationDayEnd) <= strtotime($tempDate.' '.$stpOperationDayStart)
    // e.g. 1:30 < 7:30, this means that operation date prolongs to next day till $stpOperationDayEnd
        && strtotime($tempDate.' '.$time) <= strtotime($tempDate.' '.$stpOperationDayEnd)
         // and current time less than $stpOperationDayEnd
        ){
            return (Date('Y-m-d',strtotime($operationDate)+60*60*24).' '.$time);
    } else 
        return $operationDate.' '.$time;
}

/**
 * This function returns date period string basing on start and end dates. 
 * If both dates are equal, only one date will be returned.
 * 
 * @category Data Output
 */
function showDatesPeriod($trnStartDate, $trnEndDate, $precision = 'date'){
    $strRet = (!empty($trnStartDate)
            ? ($precision == 'datetime' 
                ? $this->datetimeSQL2PHP($trnStartDate)
