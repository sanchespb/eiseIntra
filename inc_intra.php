/**
 * This function returns combination of global variable $eiseIntraKey  
 * stored at eiseIntra's inc_config.php and server variable EISINTRA_KEY that can be set in Nginx or Apache config.
 * Key length is set to 128-bit (32 bytes/chars).
 * 
 * If concatentated key length is less that 32 bytes, all remaining places are padded with zeros (0).
 *
 * When both keys are not set, function returns false and eiseIntra::encrypt() and eiseIntra::decrypt() are forced to work only as base64 encoder/decoder.
 *
 * @return string Key or false, if both key parts are empty.
 *
 */
function getEncryptionKey(){

    GLOBAL $eiseIntraKey;

    $keylen = 32;
    $key = substr($eiseIntraKey.$_SERVER['EISEINTRA_KEY'], 0, $keylen);
    $ll = $keylen - strlen($key);
    if(!$key)
        return false;
    else 
        return ($ll>0
            ? str_pad($key, $keylen, '0')
            : $key
            );
}

/**
 * Function encrypts a string with symmetric encryption 
 * using the key obtained from getEncryptionKey() method and current encoding algorithm 
 * (now RIJNDAEL_256 and mcrypt). In case mcrypt is missed in PHP or encryption key is empty, it encodes string with base64. 
 *
 * @category Authentication
 * 
 * @param string $encrypt String to be encrypted.
 *
 * @return string Encrypted string.
 */
function encrypt($encrypt) {
    $key = $this->getEncryptionKey();
    if(!$key) {
        return base64_encode($encrypt);
    }
    
    $encrypt = serialize($encrypt);
    $iv = openssl_random_pseudo_bytes(16);
    $key = pack('H*', $key);
    $mac = hash_hmac('sha256', $encrypt, substr(bin2hex($key), -32));
    $encrypted = openssl_encrypt($encrypt.$mac, 'aes-256-cbc', $key, OPENSSL_RAW_DATA, $iv);
    $encoded = base64_encode($encrypted).'|'.base64_encode($iv);
    return $encoded;
}
 
/**
 * Function decrypts a string with symmetric decryption 
 * using the key obtained from getEncryptionKey() method and current encoding algorithm 
 * (now RIJNDAEL_256 and mcrypt). In case mcrypt is missed in PHP, it encodes string with base64. 
 *
 * @category Authentication
 * 
 * @param string $encrypt String to be encrypted.
 *
 * @return string Encrypted string.
 */
function decrypt($decrypt) {
    $key = $this->getEncryptionKey();
    if(!$key) {
        return base64_decode($decrypt);
    }
    
    $parts = explode('|', $decrypt.'|');
    $decoded = base64_decode($parts[0]);
    $iv = base64_decode($parts[1]);
    
    if(strlen($iv) !== 16) return $this->decrypt_old($decrypt);
    
    $key = pack('H*', $key);
    $decrypted = openssl_decrypt($decoded, 'aes-256-cbc', $key, OPENSSL_RAW_DATA, $iv);
    
    $mac = substr($decrypted, -64);
    $decrypted = substr($decrypted, 0, -64);
    $calcmac = hash_hmac('sha256', $decrypted, substr(bin2hex($key), -32));
    
    if($calcmac !== $mac) return $this->decrypt_old($decrypt);
    return unserialize($decrypted);
}

/**
 * Special function to decrypt old-style encrypted strings with mcrypt.
 * 
 */
function decrypt_old($decrypt) {

    $key = $this->getEncryptionKey();

    if(!$key ||  !is_callable('mcrypt_decrypt')){
        return base64_decode($decrypt);
    }

    $decrypt = explode('|', $decrypt.'|');
    $decoded = base64_decode($decrypt[0]);
    $iv = base64_decode($decrypt[1]);
    if(strlen($iv)!==mcrypt_get_iv_size(MCRYPT_RIJNDAEL_256, MCRYPT_MODE_CBC)){ return false; }
    $key = pack('H*', $key);
    $decrypted = trim(mcrypt_decrypt(MCRYPT_RIJNDAEL_256, $key, $decoded, MCRYPT_MODE_CBC, $iv));
    $mac = substr($decrypted, -64);
    $decrypted = substr($decrypted, 0, -64);
    $calcmac = hash_hmac('sha256', $decrypted, substr(bin2hex($key), -32));
    if($calcmac!==$mac){ return false; }
    $decrypted = unserialize($decrypted);
    return $decrypted;
}

/**
 * This function returns password hash according to hashing method specified in $conf['pass_hash'].
 * @category Authentication
 * 
 * @param string $pass The password, the string to be encrypted.
 *
 * @return string Hash of given string.
 */
function password_hash($pass){
    switch ($this->conf['pass_hash']) {
        case 'md5':
            return md5($pass);
        case 'php_password_api':
        default:
            return password_hash($pass);
            break;
    } 
}

/**
 * This function verifies the password across the hash according to hashing method specified in $conf['pass_hash'].
 * @category Authentication
 * 
 * @param string $pass The password.
 * @param string $hash Hash string to be compared with.
 *
 * @return string Hash of given string.
 */
function password_verify($pass, $hash){
    switch ($this->conf['pass_hash']) {
        case 'md5':
            return (md5($pass)===$hash);
        case 'php_password_api':
        default:
            return password_verify($pass, $hash);
            break;
    } 
}

/**
 * Function that checks authentication with credentials database using selected $method.
 * Now it supports the following methods:
 * 1. LDAP - it checks credentials with specified GLOBAL $ldap_server with GLOBAL $ldap_domain
 * 2. database (or DB) - it checks credentials with database table stbl_user
 * 3. mysql - it checks credentials of MySQL database user supplied with $login and $password parameters. Together with authentication this 
 * Function returns true when authentication successfull, otherwise it returns false and $strError parameter 
 * variable becomes updated with authentication error message.
 *
 * LDAP method was successfully tested with Active Directory on Windows 2000, 2003, 2008, 2008R2 servers.
 *
 * @category Authentication
 * 
 * @param string $login - login name
 * @param string $password - password
 * @param string $method - authentication method. Can be 'LDAP', 'database'(equal to 'DB'), 'mysql'
 * @param array $options - Array
 *  - 'flagNoSession' (boolean) - Use true when you need one-time authentication without $_SESSION modification.
 *  - 'dbhost' (string) - Database host, used only with 'mysql' authentication method.
 *  - 'dbname' (string) - Database name. Default database to be selected with 'mysql' authentication method.
 * 
 * @return boolean authentication result: true on success, otherwise false.
 */
function Authenticate($login, $password, $method="LDAP", $options=array()){

    $oSQL = $this->oSQL;
    
    switch($method) {
    case "LDAP":    
        GLOBAL $ldap_server;
        GLOBAL $ldap_domain;
        GLOBAL $ldap_dn;
        GLOBAL $ldap_conn, $ldap_anonymous_login, $ldap_anonymous_pass;
        if (preg_match("/^([a-z0-9]+)[\/\\\]([a-z0-9]+)$/i", $login, $arrMatch)){
            $login = $arrMatch[2];
            $ldap_domain = strtolower($arrMatch[1].".e-ise.com");
        } else
            if (preg_match("/^([a-z0-9\_]+)[\@]([a-z0-9\.\-]+)$/i", $login, $arrMatch)){
                $login = $arrMatch[1];
                $ldap_domain = $arrMatch[2];
            }
            
        $ldap_conn = ldap_connect($ldap_server);
        $binding = @ldap_bind($ldap_conn, $ldap_anonymous_login, $ldap_anonymous_pass);
        
        if (!$binding){
            $method = "database";
        } else {
            $ldap_login = $login."@".$ldap_domain;
            $ldap_pass = $password;
            ldap_set_option($ldap_conn, LDAP_OPT_PROTOCOL_VERSION, 3);
            ldap_set_option($ldap_conn, LDAP_OPT_REFERRALS, 0);

            if ( !($binding = ldap_bind($ldap_conn, $ldap_login, $ldap_pass)) ){
                throw new eiseException("Bad Windows user name or password {$ldap_login}");
            } else 
                break;
        }

    case "database":
    case "DB":

        if(!$oSQL->connect()){
            throw new eiseException("Unable to connect to database");
        }
        $sqlAuth = "SELECT * FROM stbl_user WHERE usrID='{$login}'";
        $rwAuth = $oSQL->f($sqlAuth);
        if (!$rwAuth)
            throw new eiseException($this->translate("User %s not found", $login));
        if ($rwAuth['usrFlagDeleted'])
            throw new eiseException($this->translate("User %s is blocked", $login));
        if (!$this->password_verify($password, $rwAuth['usrPass']))
            throw new eiseException($this->translate("Bad user name or password %s ", $login));
            

        break;
    case "mysql":
        try {
            $this->oSQL = new eiseSQL (
                (!$options['dbhost'] ? 'localhost' : $options['dbhost'])
                , $login
                , $password
                , (!isset($options["dbname"]) || !$options["dbname"] ? 'mysql' : $options["dbname"])
                );
        } catch(Exception $e){
            throw new eiseException($e->getMessage());
        }
        break;
    } 

    if($options['flagNoSession'])
        return true;

    $this->session_initialize();
    session_regenerate_id();

    if($method=="mysql"){
        $_SESSION["DBHOST"] = $this->oSQL->dbhost;
        $_SESSION["DBPASS"] = $this->oSQL->dbpass;
    }

    $_SESSION["last_login_time"] = Date("Y-m-d H:i:s");
    $_SESSION["usrID"] = $login;
    //$_SESSION["usrID"] = 'TIKHONOVA';
    $_SESSION["authstring"] = $this->encodeAuthString($login, $password);

    $this->usrID = $_SESSION["usrID"];

    SetCookie("last_succesfull_usrID", $login, $this->conf['cookieExpire'], $this->conf['cookiePath']);

    return true;

}

/**
 * This function intialize session with session cookes placed at path set by $this->conf['cookiePath'] configuration variable.
 *
