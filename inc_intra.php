 * user function $function_name will be called and contents of $query parameter will be passed. If $query parameter is omitted, function 
 * will take $_GET global array.
 *
 * @category Data Handling
 *
 * @param variant $dataReadValues - string or array of possible &lt;input name=DataAction&gt; values that $function should handle.
 * @param string $function - callback function name.
 * @param array $query - associative array data query  
 * 
 * @return variant value that return user function.
 */
function dataRead($dataReadValues, $function=null, $arrParam = array()){

    $query = ($_SERVER['REQUEST_METHOD']=='POST' ? $_POST : (array)$_GET);

    $dataReadValues = (is_array($dataReadValues) ? $dataReadValues : array($dataReadValues));

    if(isset($query[$this->conf['dataReadKey']]) 
        && in_array($query[$this->conf['dataReadKey']], $dataReadValues)){
        
        $arrParam = func_get_args();
        array_shift($arrParam);

        if(!$function){
            $function = $query[$this->conf['dataReadKey']];
        } else {
            array_shift($arrParam);
        }
        $arrArgs = $arrParam;    
                    
        if(is_callable($function)){
            $ret = call_user_func_array($function, array_merge(Array($query), $arrParam) );
        } elseif( is_object($function) ){

            if(self::isRecursion())
                return;

            $obj = $function;
            $method = $query[$this->conf['dataReadKey']];
            $ret = array();

            try {
                if(!method_exists($obj, $method))
                    throw new Exception(sprintf("Method %s::%s() not found on dataRead()", get_class($obj), $method));
                
                $ret = call_user_func_array(array($obj, $method), array_merge(Array($query), $arrParam));
                
            } catch (Exception $e) {
                die($e->getMessage());
            }
            
        }

        if(is_array($ret)){
