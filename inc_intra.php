                $retVal .= "<option value='$key'"
                    .(isset($confOptions['optsData'][$key]) && $confOptions['optsData'][$key] ? ' data-metadata="'.htmlspecialchars($confOptions['optsData'][$key]).'"' : '')
                    .((string)$key==(string)$strValue
                    || (is_integer($key) && (string)$value==(string)$strValue)
                    || $key===$strValue
                        ? " SELECTED " 
                        : "").
                        (in_array($key, $confOptions['deletedOptions']) ? ' class="deleted"' : '').
                        ">".str_repeat('&nbsp;',5*(isset($confOptions["indent"][$key]) ? $confOptions["indent"][$key] : 0)).htmlspecialchars($value)."</option>\r\n";
        }
        $retVal .= ($confOptions['defaultTextPosition']=='last' ? $optDefaultText : '');
        $retVal .= "</select>";

    } else {
        
        $arrOptions = new RecursiveIteratorIterator(new RecursiveArrayIterator($arrOptions));
        $valToShow = '';
        $textToShow = '';
        foreach ($arrOptions as $key => $value){
            if ((string)$key==(string)$strValue) {
               $valToShow = $value;
               $textToShow = $key;
               break;
            }
        }
        $valToShow=($valToShow!="" ? $valToShow : $confOptions["defaultText"]);
        
        $retVal = "<div id=\"span_{$id}\""
            .($strClass ? ' class="'.$strClass.'"' : "")
            .'>'
            .($confOptions['href'] ? "<a href=\"{$confOptions['href']}\"".($confOptions["target"] ? " target=\"{$confOptions["target"]}\"" : '').">" : '')
            .htmlspecialchars($valToShow)
            .($confOptions['href'] ? '</a>' : '')
            ."</div>\r\n".
        "<input type=\"hidden\" name=\"{$strName}\" id=\"{$id}\" class=\"eif-input\"".
        " value=\"".htmlspecialchars($textToShow)."\" />\r\n";
        
    
    }
